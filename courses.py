# ======= Helper Functions =======

def get_courses_for_role(category: str, role: str):
    """
    Get a list of courses for the given category and role.
    Returns an empty list if the category or role is not found.
    
    Args:
        category (str): The category name.
        role (str): The role/job title.
    
    Returns:
        list: A list of [course title, URL] pairs.
    """
    return COURSES_BY_CATEGORY.get(category, {}).get(role, [])
def get_all_course_recommendations():
    recommendations = []
    for category, roles in COURSES_BY_CATEGORY.items():
        for role, courses in roles.items():
            for course_title, course_url in courses:
                recommendations.append({
                    "name": course_title,
                    "link": course_url,
                    "category": category,
                    "role": role
                })
    return recommendations
